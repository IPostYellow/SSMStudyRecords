package pojo;

import org.springframework.beans.factory.annotation.Value;

public class User {
    @Value("张三")
    public String name;

    @Override
    public String toString() {
        return "User{" +
                "name='" + name + '\'' +
                '}';
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
